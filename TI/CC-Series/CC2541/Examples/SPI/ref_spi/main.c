#include "globals.h"

    //Locals
    uint16_t i = 0;
    uint8_t rx_val = 0x00;


/************************************************************************************************************************************/
/** @fcn        int main(void)
 *  @brief      x
 *  @details    x
 */
/************************************************************************************************************************************/
int main(void) {

    //Init
     sys_init();

    //PARTNUMBER
    for(;;) {
        //CS LOW
        P1 &= ~BIT7;
        RADIO_PORT &= ~RADIO_PIN_CS_N;
        
        //0xEF
        spi_transmit(0xEF);                                                 /* 0xEF = b7+ (b7 | b6 | 0x2F)                          */
        
        //0x8F
        spi_transmit(0x8F);                                                 /* 0x8F = Extended Address                              */
        
        //0x00
        spi_transmit(0x00);                                                 /* D.C.                                                 */
        rx_val = U0DBUF;                                                    /* grab the part number response                        */
        
        //CS HIGH
        RADIO_PORT |= RADIO_PIN_CS_N;
        P1 |= BIT7;
        
        asm(" NOP");                                                        /* for debug breakpoints                                */
        
        //Small Delay
        for(i=0; i<50000; i++) {
            asm(" NOP");                                                    /* set to 20 kHz                                        */
        }
    }
}


/************************************************************************************************************************************/
/** @fcn        void sys_init(void)
 *  @brief      init system on boot
 *  @details    x
 */
/************************************************************************************************************************************/
void sys_init(void) {


    //******************************************************************************************************************************//
    //                                                      CLOCK                                                                   //
    //******************************************************************************************************************************//
    CLKCONCMD = (CLKCONCMD & ~(CLKCON_OSC | CLKCON_CLKSPD)) | CLKCON_CLKSPD_32M;        // Set to HS XOSC, no pre-scaling           */
    while (CLKCONSTA & CLKCON_OSC);                                         /* Wait until clock source has changed                  */

    
    //******************************************************************************************************************************//
    //                                                       I/O                                                                    //
    //******************************************************************************************************************************//
    spi_gpio_init();

    P1DIR |= BIT7;                                                          /* Debug Pin                                            */
    P1 |= BIT7;
    

    //******************************************************************************************************************************//
    //                                                       SPI                                                                    //
    //******************************************************************************************************************************//
    spi_init();


    //******************************************************************************************************************************//
    //                                                       RADIO                                                                  //
    //******************************************************************************************************************************//

    
    
    return;
}


/************************************************************************************************************************************/
/** @fcn        void std_delay(void)
 *  @brief      a small unquantified delay
 *  @details    x
 */
/************************************************************************************************************************************/
void std_delay(void) {
  
  volatile uint16_t i = 0;
  
  for(i=0; i<5; i++) {
    asm(" NOP");
  }
  
  return;
}
 
